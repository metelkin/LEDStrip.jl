using LEDStrip

colors = [
    0xff0000, 0x00ff00, 0x0000ff,
    0x000000, 0x000000, 0x000000,
    0xff0000, 0x00ff00, 0x0000ff,
    0x000000, 0x000000, 0x000000,
    0xff0000, 0x00ff00, 0x0000ff,
    0x000000, 0x000000, 0x000000,
    0xff0000, 0x00ff00, 0x0000ff,
    0x000000, 0x000000, 0x000000,
]

@info "Starting..."

s = Strip(1; led_count = 100)
set_colors!(s, colors)

for i in 1:10
    show_colors(s)
    sleep(2)
    hide_colors(s)
    sleep(1)
end

@info "Stop."
